// Generates supabase/functions/enquiry/templates.ts from the canonical email
// templates so the live function's HTML stays in sync with Docs/email-templates/.
// Run after editing 01/02, then redeploy the function:
//   node scripts/build-enquiry-emails.mjs
//   supabase functions deploy enquiry --no-verify-jwt
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, 'Docs/email-templates');
const OUT = path.join(ROOT, 'supabase/functions/enquiry/templates.ts');
const read = (f) => fs.readFileSync(path.join(DIR, f), 'utf8');

const AUTO_HTML = read('01-enquiry-autoreply.html');
const AUTO_TEXT = read('01-enquiry-autoreply.txt');
const NOTIFY_HTML = read('02-new-lead-notification.html');
const NOTIFY_TEXT = read('02-new-lead-notification.txt');

const ts = `// AUTO-GENERATED by scripts/build-enquiry-emails.mjs — DO NOT EDIT BY HAND.
// Source of truth: Docs/email-templates/{01-enquiry-autoreply,02-new-lead-notification}.{html,txt}
// Regenerate after editing those, then redeploy the enquiry function.

const AUTOREPLY_HTML = ${JSON.stringify(AUTO_HTML)};
const AUTOREPLY_TEXT = ${JSON.stringify(AUTO_TEXT)};
const NOTIFY_HTML = ${JSON.stringify(NOTIFY_HTML)};
const NOTIFY_TEXT = ${JSON.stringify(NOTIFY_TEXT)};

const escHtml = (s: string): string =>
  s.replace(/[&<>"']/g, (c) =>
    ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]!),
  );

// Replace {{snake_case}} tokens; HTML-escape values only for the HTML body.
function fill(tpl: string, vars: Record<string, string>, isHtml: boolean): string {
  return tpl.replace(/\\{\\{(\\w+)\\}\\}/g, (_m, k) => {
    const v = vars[k] ?? "";
    return isHtml ? escHtml(v) : v;
  });
}

export interface AutoreplyVars { first_name: string; business: string; }
export interface NotifyVars {
  business: string; name: string; first_name: string; email: string;
  message: string; lead_id: string; submitted_at: string;
}

export function renderAutoreply(v: AutoreplyVars): { html: string; text: string } {
  const r = v as unknown as Record<string, string>;
  return { html: fill(AUTOREPLY_HTML, r, true), text: fill(AUTOREPLY_TEXT, r, false) };
}

export function renderNotification(v: NotifyVars): { html: string; text: string } {
  const r = v as unknown as Record<string, string>;
  return { html: fill(NOTIFY_HTML, r, true), text: fill(NOTIFY_TEXT, r, false) };
}
`;

fs.writeFileSync(OUT, ts);
console.log('wrote', path.relative(ROOT, OUT), '(' + (ts.length / 1024).toFixed(1) + ' KB)');
