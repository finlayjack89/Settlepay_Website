// Generates a self-contained templates.ts (from the canonical Docs/email-templates/)
// into every function that sends booking email, so the HTML stays in sync.
// Run after editing the templates, then redeploy the affected functions:
//   node scripts/build-booking-emails.mjs
//   supabase functions deploy book --no-verify-jwt   (and cal-webhook, booking-manage, booking-reminders)
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, 'Docs/email-templates');
const FN = path.join(ROOT, 'supabase/functions');
const read = (f) => fs.readFileSync(path.join(DIR, f), 'utf8');

const CONFIRM_HTML = read('03-consultation-confirmation.html');
const CONFIRM_TEXT = read('03-consultation-confirmation.txt');
const REMIND_HTML = read('03b-consultation-reminder.html');
const REMIND_TEXT = read('03b-consultation-reminder.txt');
const CANCEL_HTML = read('12-consultation-cancelled.html');
const CANCEL_TEXT = read('12-consultation-cancelled.txt');

const ts = `// AUTO-GENERATED by scripts/build-booking-emails.mjs — DO NOT EDIT BY HAND.
// Source of truth: Docs/email-templates/{03,03b,12}-*.{html,txt}

const CONFIRM_HTML = ${JSON.stringify(CONFIRM_HTML)};
const CONFIRM_TEXT = ${JSON.stringify(CONFIRM_TEXT)};
const REMIND_HTML = ${JSON.stringify(REMIND_HTML)};
const REMIND_TEXT = ${JSON.stringify(REMIND_TEXT)};
const CANCEL_HTML = ${JSON.stringify(CANCEL_HTML)};
const CANCEL_TEXT = ${JSON.stringify(CANCEL_TEXT)};

const escHtml = (s: string): string =>
  s.replace(/[&<>"']/g, (c) =>
    ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]!),
  );

// Replace {{snake_case}} tokens; HTML-escape values only for the HTML body.
function fill(tpl: string, vars: Record<string, string>, isHtml: boolean): string {
  return tpl.replace(/\\{\\{(\\w+)\\}\\}/g, (_m, k) => {
    const v = vars[k] ?? "";
    return isHtml ? escHtml(v) : v;
  });
}

export interface BookingVars {
  first_name: string;
  date: string;
  time: string;
  join_url: string;
  manage_url: string;
}
export interface CancelVars {
  first_name: string;
  date: string;
  time: string;
  rebook_url: string;
}

export function renderBookingConfirmation(v: BookingVars): { html: string; text: string } {
  const vars = v as unknown as Record<string, string>;
  return { html: fill(CONFIRM_HTML, vars, true), text: fill(CONFIRM_TEXT, vars, false) };
}
export function renderBookingReminder(v: BookingVars): { html: string; text: string } {
  const vars = v as unknown as Record<string, string>;
  return { html: fill(REMIND_HTML, vars, true), text: fill(REMIND_TEXT, vars, false) };
}
export function renderBookingCancel(v: CancelVars): { html: string; text: string } {
  const vars = v as unknown as Record<string, string>;
  return { html: fill(CANCEL_HTML, vars, true), text: fill(CANCEL_TEXT, vars, false) };
}
`;

const TARGETS = ['book', 'booking-manage', 'booking-reminders'];
let n = 0;
for (const fn of TARGETS) {
  const dir = path.join(FN, fn);
  if (!fs.existsSync(dir)) continue;
  fs.writeFileSync(path.join(dir, 'templates.ts'), ts);
  n++;
  console.log('templates.ts ->', fn);
}
console.log(`Done (${n} files, ${(ts.length / 1024).toFixed(1)} KB each).`);
