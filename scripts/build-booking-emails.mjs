// Generates supabase/functions/cal-webhook/templates.ts from the canonical
// consultation-confirmation template, so the webhook's HTML stays in sync with
// Docs/email-templates/. Run after editing 03, then redeploy the function:
//   node scripts/build-booking-emails.mjs
//   supabase functions deploy cal-webhook --no-verify-jwt
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, 'Docs/email-templates');
const OUT = path.join(ROOT, 'supabase/functions/cal-webhook/templates.ts');
const read = (f) => fs.readFileSync(path.join(DIR, f), 'utf8');

const CONFIRM_HTML = read('03-consultation-confirmation.html');
const CONFIRM_TEXT = read('03-consultation-confirmation.txt');

const ts = `// AUTO-GENERATED by scripts/build-booking-emails.mjs — DO NOT EDIT BY HAND.
// Source of truth: Docs/email-templates/03-consultation-confirmation.{html,txt}
// Regenerate after editing those, then redeploy the cal-webhook function.

const CONFIRM_HTML = ${JSON.stringify(CONFIRM_HTML)};
const CONFIRM_TEXT = ${JSON.stringify(CONFIRM_TEXT)};

const escHtml = (s: string): string =>
  s.replace(/[&<>"']/g, (c) =>
    ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]!),
  );

// Replace {{snake_case}} tokens; HTML-escape values only for the HTML body.
function fill(tpl: string, vars: Record<string, string>, isHtml: boolean): string {
  return tpl.replace(/\\{\\{(\\w+)\\}\\}/g, (_m, k) => {
    const v = vars[k] ?? "";
    return isHtml ? escHtml(v) : v;
  });
}

export interface BookingVars {
  first_name: string;
  date: string;
  time: string;
  join_url: string;
}

export function renderBookingConfirmation(v: BookingVars): { html: string; text: string } {
  const vars = v as unknown as Record<string, string>;
  return { html: fill(CONFIRM_HTML, vars, true), text: fill(CONFIRM_TEXT, vars, false) };
}
`;

fs.writeFileSync(OUT, ts);
console.log('Wrote', path.relative(ROOT, OUT), `(${(ts.length / 1024).toFixed(1)} KB)`);
